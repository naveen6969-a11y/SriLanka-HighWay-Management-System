    private void jRadioButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton2ActionPerformed
                 String vehicleNo = jTextField1.getText().trim();
    Object selected = jComboBox2.getSelectedItem();
    if (selected == null || vehicleNo.isEmpty()) {
        jLabel5.setText("Enter vehicle number and select location.");
        return;
    }
    String exitLocation = selected.toString();

    
    processor = new VehicleExitProcessor();

    if (processor.calculateExit(vehicleNo, exitLocation)) {
        jLabel5.setText("Total Cost: Rs. " + processor.getLastTotalCost());
    } else {
        jLabel5.setText("No active entrance found.");
        processor = null; 
    }        
    }//GEN-LAST:event_jRadioButton2ActionPerformed

    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
           String vehicleNo = jTextField2.getText().trim();
    String location = jComboBox1.getSelectedItem().toString();
    String reportType = jComboBox3.getSelectedItem().toString();
    String description = jTextArea2.getText().trim();

    if (description.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Enter report description");
        return;
    }

    if (reportType.equalsIgnoreCase("Accident") && vehicleNo.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vehicle number required for accident report");
        return;
    }

    
    String cashierName =Session.getUsername();

    CashierReportAdder adder = new CashierReportAdder();

    if (adder.submitReport(reportType, description, vehicleNo, location, cashierName)) {

        JOptionPane.showMessageDialog(this, "Report submitted successfully");

        
        jTextArea2.setText("");
        jComboBox3.setSelectedIndex(0);

    } else {
        JOptionPane.showMessageDialog(this, "Failed to submit report");
    }         
    }//GEN-LAST:event_jButton9ActionPerformed

