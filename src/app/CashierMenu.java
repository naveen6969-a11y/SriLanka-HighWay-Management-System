    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
           String vehicleNo = jTextField1.getText().trim();

if (vehicleNo.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Enter vehicle number");
    return;
}

if (jComboBox2.getSelectedItem() == null) {
    JOptionPane.showMessageDialog(this, "Select a location");
    return;
}

String location = jComboBox2.getSelectedItem().toString();

if (jRadioButton1.isSelected()) {

    VehicleEventAdder adder = new VehicleEventAdder();
    if (adder.addEntrance(vehicleNo, location)) {
        JOptionPane.showMessageDialog(this, "Vehicle entered successfully");
            jTextField1.setText("");
            jComboBox2.setSelectedIndex(0);
            jRadioButton1.setSelected(true);
             refreshCashierTable();
    } else {
        JOptionPane.showMessageDialog(this, "Error recording entrance");
    }

} else if (jRadioButton2.isSelected()) {

   

    if (processor != null && processor.submitExit()) {
    JOptionPane.showMessageDialog(this, "Exit recorded successfully! Cost: Rs. " + processor.getLastTotalCost());
    jTextField1.setText("");
            jComboBox2.setSelectedIndex(0);
            jRadioButton1.setSelected(true);
    jLabel5.setText("");
    processor = null; 
    refreshCashierTable();
} else {
    JOptionPane.showMessageDialog(this, "Failed to record exit.");
}}
    }//GEN-LAST:event_jButton7ActionPerformed

