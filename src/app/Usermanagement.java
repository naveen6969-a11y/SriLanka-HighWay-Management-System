private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) { 
        int row = jTable2.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Please select a cashier to delete.");
        return;
    }

    String username = jTable2.getValueAt(row, 0).toString();

    int confirm = JOptionPane.showConfirmDialog(this,
            "Are you sure you want to delete cashier: " + username + "?",
            "Confirm Delete",
            JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        CashierDeleter deleter = new CashierDeleter();
        if (deleter.delete(username)) {
            JOptionPane.showMessageDialog(this, "Cashier deleted successfully!");
            new CashierTableLoader().loadTable(jTable2);
        } else {
            JOptionPane.showMessageDialog(this, "Error deleting cashier.");
        }
    }
}
