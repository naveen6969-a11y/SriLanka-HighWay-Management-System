private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        String username = JOptionPane.showInputDialog(this, "Enter new username:");
    if (username == null || username.isEmpty()) return;

    String password = JOptionPane.showInputDialog(this, "Enter password:");
    if (password == null || password.isEmpty()) return;

    UserAdder adder = new UserAdder();
    if (adder.add(username, password)) {
        JOptionPane.showMessageDialog(this, "User added successfully!");
        new UserTableLoader().loadTable(jTable1);
    } else {
        JOptionPane.showMessageDialog(this, "Error adding user.");
    }

}
